# Import math Library
import math
#----------------------------------------------------------------
# 弧度转度
#----------------------------------------------------------------
def Rad2Deg(rad):
    return rad * 180.0 / math.pi

#----------------------------------------------------------------
# 度转弧度
#----------------------------------------------------------------
def Deg2Rad(deg):
    return deg * math.pi / 180.0

#----------------------------------------------------------------
# 英寸转米
#----------------------------------------------------------------
def Feet2Meter(val):
    return 0.3048 * val

#----------------------------------------------------------------
# 米转英寸
#----------------------------------------------------------------
def Meter2Feet(val):
    return val / 0.3048

